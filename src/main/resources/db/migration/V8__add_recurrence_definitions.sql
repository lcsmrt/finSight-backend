--
-- Recurring/Installment Series Editing (V8) — introduces recurrence_definitions, the
-- schedule-defining "parent" of a series of financial_transactions occurrences, and
-- recurrence_definition_participants, its split-of-record. Adds a nullable
-- financial_transactions.recurrence_definition_id FK linking each occurrence back to its
-- definition. Backfill is metadata-only: it derives one definition per distinct existing
-- series_id from that series' EARLIEST occurrence and links occurrences to it — it never
-- rewrites any occurrence's amount, description, category_id, or participant rows.
-- Pure SQL only.
--

--
-- Name: recurrence_definitions; Type: TABLE; Schema: public; Owner: -
--

CREATE TABLE public.recurrence_definitions (
    id bigint NOT NULL,
    plan_id bigint NOT NULL,
    created_by bigint NOT NULL,
    category_id bigint,
    series_id character varying(255) NOT NULL,
    type character varying(16) NOT NULL,
    amount numeric(38,2) NOT NULL,
    description character varying(255) NOT NULL,
    mode character varying(16) NOT NULL,
    recurrence_interval character varying(16),
    parcels_number integer,
    first_parcel integer,
    start_date date NOT NULL,
    end_date date,
    split_mode character varying(16) NOT NULL DEFAULT 'EQUAL',
    generated_through date
);


ALTER TABLE public.recurrence_definitions ALTER COLUMN id ADD GENERATED BY DEFAULT AS IDENTITY (
    SEQUENCE NAME public.recurrence_definitions_id_seq
    START WITH 1
    INCREMENT BY 1
    NO MINVALUE
    NO MAXVALUE
    CACHE 1
);


ALTER TABLE ONLY public.recurrence_definitions
    ADD CONSTRAINT recurrence_definitions_pkey PRIMARY KEY (id);


ALTER TABLE ONLY public.recurrence_definitions
    ADD CONSTRAINT uk_recurrence_definitions_series_id UNIQUE (series_id);


ALTER TABLE ONLY public.recurrence_definitions
    ADD CONSTRAINT fk_recdef_plan FOREIGN KEY (plan_id) REFERENCES public.plans(id);


ALTER TABLE ONLY public.recurrence_definitions
    ADD CONSTRAINT fk_recdef_created_by FOREIGN KEY (created_by) REFERENCES public.users(id);


ALTER TABLE ONLY public.recurrence_definitions
    ADD CONSTRAINT fk_recdef_category FOREIGN KEY (category_id) REFERENCES public.financial_transaction_categories(id);


CREATE INDEX idx_recdef_plan_id ON public.recurrence_definitions USING btree (plan_id);
CREATE INDEX idx_recdef_series_id ON public.recurrence_definitions USING btree (series_id);


--
-- Name: recurrence_definition_participants; Type: TABLE; Schema: public; Owner: -
--

CREATE TABLE public.recurrence_definition_participants (
    id bigint NOT NULL,
    recurrence_definition_id bigint NOT NULL,
    member_user_id bigint NOT NULL,
    share_amount numeric(38,2) NOT NULL
);


ALTER TABLE public.recurrence_definition_participants ALTER COLUMN id ADD GENERATED BY DEFAULT AS IDENTITY (
    SEQUENCE NAME public.recurrence_definition_participants_id_seq
    START WITH 1
    INCREMENT BY 1
    NO MINVALUE
    NO MAXVALUE
    CACHE 1
);


ALTER TABLE ONLY public.recurrence_definition_participants
    ADD CONSTRAINT recurrence_definition_participants_pkey PRIMARY KEY (id);


ALTER TABLE ONLY public.recurrence_definition_participants
    ADD CONSTRAINT uk_recdef_participants_def_member UNIQUE (recurrence_definition_id, member_user_id);


ALTER TABLE ONLY public.recurrence_definition_participants
    ADD CONSTRAINT fk_recdef_participants_definition FOREIGN KEY (recurrence_definition_id) REFERENCES public.recurrence_definitions(id) ON DELETE CASCADE;


ALTER TABLE ONLY public.recurrence_definition_participants

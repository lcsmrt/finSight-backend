--
-- financial_transactions: add nullable recurrence_definition_id FK column
--

ALTER TABLE public.financial_transactions
    ADD COLUMN recurrence_definition_id bigint;

ALTER TABLE ONLY public.financial_transactions
    ADD CONSTRAINT fk_ft_recurrence_definition FOREIGN KEY (recurrence_definition_id) REFERENCES public.recurrence_definitions(id);

CREATE INDEX idx_ft_recurrence_definition_id ON public.financial_transactions USING btree (recurrence_definition_id);


--
-- Backfill (metadata-only): one recurrence_definitions row per distinct existing series_id,
-- derived from that series' earliest occurrence. Never touches amount/description/category_id/
-- participants of any occurrence — only populates the new parent row and the new FK column.
--

WITH series_bounds AS (
    SELECT series_id, MIN(start_date) AS min_date, MAX(start_date) AS max_date
    FROM public.financial_transactions
    WHERE series_id IS NOT NULL
    GROUP BY series_id
),
earliest AS (
    SELECT DISTINCT ON (series_id) *
    FROM public.financial_transactions
    WHERE series_id IS NOT NULL
    ORDER BY series_id, start_date
)
INSERT INTO public.recurrence_definitions (
    plan_id, created_by, category_id, series_id, type, amount, description,
    mode, recurrence_interval, parcels_number, first_parcel, start_date, end_date, split_mode
)
SELECT
    e.plan_id,
    e.created_by,
    e.category_id,
    e.series_id,
    e.type,
    e.amount,
    regexp_replace(e.description, '\s*\(\d+/\d+\)\s*$', ''),
    CASE WHEN e.parcels_number IS NOT NULL THEN 'INSTALLMENT' ELSE 'RECURRING' END,
    'MONTHLY',
    e.parcels_number,
    CASE WHEN e.parcels_number IS NOT NULL
         THEN COALESCE(substring(e.description from '\((\d+)/\d+\)')::int, 1)
         ELSE NULL
    END,
    e.start_date,
    CASE WHEN e.parcels_number IS NOT NULL THEN NULL ELSE sb.max_date END,
    e.split_mode
FROM earliest e
JOIN series_bounds sb ON sb.series_id = e.series_id;


-- Link every occurrence of a series back to its new recurrence_definitions parent.
UPDATE public.financial_transactions ft
    SET recurrence_definition_id = rd.id
    FROM public.recurrence_definitions rd
    WHERE ft.series_id = rd.series_id;


-- Seed each definition's participant split from its earliest occurrence's own participants.
WITH earliest AS (
    SELECT DISTINCT ON (series_id) id, series_id
    FROM public.financial_transactions
    WHERE series_id IS NOT NULL
    ORDER BY series_id, start_date
)
INSERT INTO public.recurrence_definition_participants (recurrence_definition_id, member_user_id, share_amount)
SELECT rd.id, tp.member_user_id, tp.share_amount
FROM public.recurrence_definitions rd
JOIN earliest e ON e.series_id = rd.series_id
JOIN public.transaction_participants tp ON tp.transaction_id = e.id;
